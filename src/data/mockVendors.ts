export const favoriteVendorIds = favorites
  .filter((favorite) => favorite.userId === 'user-customer-alex')
  .map((favorite) => favorite.vendorId);

export const vendorTabRecords: VendorTabRecord[] = vendorBusinesses.map((vendor) => {
  const owner = users.find((user) => user.id === vendor.userId)!;
  const latestLocation =
    vendorLocations.find((location) => location.vendorId === vendor.id) ?? null;
  const vendorProducts = products.filter((product) => product.vendorId === vendor.id);
  const vendorFavorites = favorites.filter((favorite) => favorite.vendorId === vendor.id);

  return {
    vendor,
    owner,
    latestLocation,
    products: vendorProducts,
    isFavorite: favoriteVendorIds.includes(vendor.id),
    favoriteCount: vendorFavorites.length,
  };
});

export const vendors: Vendor[] = vendorBusinesses.map((vendor) => {
  const latestLocation =
    vendorLocations.find((location) => location.vendorId === vendor.id) ?? null;
  const vendorMenu = products
    .filter((product) => product.vendorId === vendor.id)
    .map((product) => ({
      id: product.id,
      name: product.name,
      priceLabel: product.priceLabel,
    }));

  return {
    id: vendor.id,
    name: vendor.businessName,
    category: vendor.category,
    description: vendor.description,
    distanceKm: vendor.distanceKm,
    isOpen: vendor.isOpen,
    rating: vendor.rating,
    lastUpdated: latestLocation?.recordedAt ?? 'No update',
    priceHint: vendor.priceHint,
    tags: vendor.tags,
    eta: vendor.eta,
    liveStatus: vendor.liveStatus,
    nextArea: vendor.nextArea,
    menu: vendorMenu,
  };
});
